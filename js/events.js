function getXMLHTTPRequest() {
    var req = false;
    try {
        req = new XMLHttpRequest();
    } catch(err1) {
        try {
            req = new ActiveXObject("Msxml2.XMLHTTP");
        } catch(err2) {
            try {
                req = new ActiveXObject("Microsoft.XMLHTTP");
            } catch(err3) {
                req = false;
            }
        }
    }
    return req;
}

function callAjax(event) {
    if(!event) {
        var event = window.event;
    }
    if(event.srcElement) {
        obj = event.srcElement;
        //alert("1) callAjax: \nobj.name=" + obj.name + "\nobj.id=" + obj.id);
    } else {
        obj = event.target;
        //alert("2) callAjax: \nobj.name=" + obj.name + "\nobj.id=" + obj.id);
    }
    
    if(obj.name == "logo_type") {
        if(getLogoType(obj.id)) {
            logo_type = obj.id;
        }
    }
    if(obj.name == "logo_name") {
        if(getLogoName(obj.id)) {
            logo_name = obj.id;
        }
    }
    
    if(obj.name == "logo_name" || obj.name == "logo_type") {
		//alert("callAjax: \nobj.name=" + obj.name);
        base_url = '.';
        if(logo_type != "" && logo_name != "") {
            //alert("1");
            var url = base_url + "?logo_name=" + logo_name + "&logo_type=" + logo_type;
            //alert("url=" + url);
            myRequest.open("GET", url + "&rand=", true);
            myRequest.onreadystatechange = responseAjax;
            myRequest.send(null);
        } else if(logo_type != "") {
            //alert("2");
            var url = base_url + "?logo_type=" + logo_type;
            //alert("url=" + url);
            myRequest.open("GET", url + "&rand=", true);
            myRequest.onreadystatechange = responseAjax;
            myRequest.send(null);
        } else if(logo_name != "") {
            //alert("3");
            var url = base_url + "?logo_name=" + logo_name;
            //alert("url=" + url);
            myRequest.open("GET", url + "&rand=", true);
            myRequest.onreadystatechange = responseAjax;
            myRequest.send(null);
        }
        
    }

    function responseAjax() {
        if (myRequest.readyState == 4) {
            if (myRequest.status == 200) {
                var temp = myRequest.responseText;
                temp = temp.split('<!-- begin logos app -->');
                temp = temp[1];
                temp = temp.split('<!-- end logos app -->');
                temp = temp[0];
                document.getElementById("logos").innerHTML = temp;
            } else {
                alert("An error has occurred: " + myRequest.statusText);
            }
        }
    }
}

